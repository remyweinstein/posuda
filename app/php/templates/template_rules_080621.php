      </li><li>2.	Бонусы начисляются при авторизации карты до момента оплаты покупки на кассе путем считывания QR-кода на карте, в мобильном приложении или по номеру мобильного телефона, указанного участником при заполнении анкеты. % начисления бонусов указан в Приложении 1.
