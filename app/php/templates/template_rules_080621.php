      </li><li>8.	В случае возврата товара израсходованные бонусы восстанавливаются на бонусном счете участника; начисленные за покупку бонусы аннулируются. При возврате части покупки использованные бонусы восстанавливаются пропорционально сумме возврата (стоимости товара).
