    <h5>Правила списания бонусов</h5>
    <ul>
      <li>1.	Начисленные бонусы используются в виде скидки при оплате товаров.
      </li><li>2.	Списать можно только начисленные и активные бонусы. Срок активации базовых бонусов – 36 часов. За одну операцию можно произвести как списание, так и начисление бонусов.        Сначала происходит списание бонусов (от общей суммы покупки) в зависимости от процента списания, указанного на ценнике, или желаемой суммы бонусов к списанию, затем начисляется установленный процент от стоимости товара, подлежащей оплате в рублях после уменьшения цены товара на сумму списанных бонусов. Если в чеке несколько позиций товара с разным процентом списания, то бонусы списываются согласно проценту списания, установленного на данный товар. Если количество списываемых бонусов меньше разрешенного к списанию, то бонусы распределяются пропорционально.
      </li><li>3.	Для расходования бонусов участник должен пройти идентификацию. Идентификация осуществляется одним из следующих способов:
      </li><li>- через мобильное приложение «Мир посуды» путем сканирования QR-кода.  
      </li><li>- путем предъявления карты кассиру в магазине. 
      </li><li>- с помощью SMS-авторизации по привязанному к карте номеру телефона. Клиенту приходит на зарегистрированный в бонусной программе номер телефона смс-сообщение с кодом. Код сообщается кассиру. Кассир вносит в программу код и авторизует участника.
      </li><li>4.	Накопленные бонусы можно списать как полностью, так и частично, по желанию участника, но не ниже установленного уровня минимальных розничных цен (далее – МРЦ), установленных Правительством РФ на отдельны виды товара, и не более установленного максимального процента скидки, указанного на ценнике.
