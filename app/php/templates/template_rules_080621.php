      </li><li>5.	На товары, участвующие в бонусной программе, начисляются бонусы от стоимости товара, оплаченной рублями. На часть стоимости товара, оплаченную бонусами, вознаграждение не начисляется. Максимальный размер бонусов, начисляемых при покупке товара, указан на ценнике. 
