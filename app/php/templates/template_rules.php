      </ol>
      <p>Бонусы, потраченные до момента блокировки счета, не восстанавливаются.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
      <ol start="3">
          <li>Оператор не несет ответственности за любые убытки участника программы, включая, но не ограничиваясь убытками, нанесенными в результате несанкционированного использования карты и доступа к бонусному счету по вине и/или небрежности участника и/или без таковых.</li>
          <li>Карта автоматически блокируется оператором ПЛ в случае превышения лимита в количестве 10 операций в день.</li>
      </ol>
