      <p style="text-align: center"><u>Блокировка Бонусной Карты</u></p>
      <ol>
          <li>При наличии сомнений в законности проведенных по карте операций оператор программы оставляет за собой право заблокировать карту до выяснения обстоятельств. О блокировке карты участнику направляется смс-уведомление.</li>
