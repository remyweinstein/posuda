      <h3>Правила программы лояльности магазинов «Мир посуды» <br> ред. от 10.08.2022</h3>
      <p style="text-align: center"><u>Общие положения</u></p>
      <p>Программа лояльности (далее &ndash; &laquo;ПЛ&raquo;) действует во всех магазинах, использующих товарный знак &laquo;Мир посуды&raquo; по лицензионному договору (далее по тексту &ndash; &laquo;Компания&raquo;).</p>
      <p>Участником программа лояльности может стать любое физическое лицо, достигшее 18-летнего возраста (далее &ndash; &laquo;Участник&raquo;).</p>
      <p>Участнику выдается пластиковая и/или присваивается виртуальная карта, которую можно завести в мобильном приложении (далее &ndash; &laquo;МП&raquo;).</p>
      <p>В МП содержится информация о данных участника, акциях, новости, отражается баланс карты и детализация расчетов (информация о фактах списания/начисления бонусов).</p>
      <p>Участник программы лояльности может оплатить часть стоимости покупки из расчета 1 бонус = 1 рубль, максимальный процент указан на ценнике. Товар, на который не распространяется действие программы лояльности или распространятся только в части начислений, указан в Приложении № 2.</p>
	  <p>Для участия в программе обязательны наличие актуального номера мобильной связи (телефона) и дата рождения.</p>
