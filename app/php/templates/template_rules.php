На одно физическое лицо может быть оформлена только одна карта каждого вида (одна виртуальная и одна пластиковая). При выявлении нескольких карт одного вида, оформленных на одного участника, организатор программы вправе заблокировать все, кроме карты с наименьшим балансом, с аннулированием накопленных на блокируемых картах бонусов без возможности их восстановления.
</li>
