      <p style="text-align: center"><u>Правила начисления бонусов</u></p>
      <ol>
          <li>Бонусы начисляются при совершении участником ПЛ покупки товаров в магазинах Компании, с использованием пластиковой либо виртуальной карты участника ПЛ статуса Бонусная.</li>
          <li>Для начисления бонусов необходимо авторизоваться в ПЛ до момента оплаты покупки на кассе путем считывания QR-кода на пластиковой либо виртуальной карте, или по номеру мобильного телефона, указанного участником при заполнении анкеты. Процент начисления бонусов указан на ценнике на товар, максимальный процент начисления указан в Приложении № 1.</li>
          <li>Бонусы не начисляются при покупке товаров, не участвующих в ПЛ (Приложение № 1).</li>
          <li>После совершения покупки и оплаты товара бонусы автоматически начисляются на Бонусный счет участника при условии прохождения авторизации. Бонусы становятся активными через 24 часа после оплаты товара.</li>
          <li>Начисление бонусов за покупки товаров, участвующих в ПЛ происходит исходя из стоимости товара, оплаченной рублями. На часть стоимости товара, оплаченную бонусами, вознаграждение не начисляется. Максимальный размер бонусов, который может быть начислен при покупке товара, указан на ценнике.</li>
          <li>Срок действия (хранения) начисленных за покупки бонусов до момента их сгорания составляет 90 (девяносто) дней c момента их активации.</li>
          <li>За семь дней до указанного при регистрации в программе дня рождения участника начисляются поздравительные 1000 (одна тысяча) бонусов. Бонусы можно использовать за семь дней, в день рождения, и семь дней после дня рождения. Неиспользованные в указанный период поздравительные бонусы аннулируются без возможности их восстановления.</li>
          <li>Проверить бонусный баланс можно самостоятельно в мобильном приложении либо запросить информацию на кассе в магазине Компании, предъявив карту или пройдя идентификацию по номеру мобильного телефона, зарегистрированного в ПЛ.</li>
          <li>Бонусы начисляются в повышенном размере при совершении покупки на сумму более 5000 (пять тысяч) рублей в следующем порядке:</li>
      </ol>
      <p>При совершении покупки на сумму более 5000 (пяти тысяч) рублей с предъявлением бонусной карты ПЛ, на бонусный счет участника начисляются бонусы в количестве, соответствующем итоговой сумме денежных средств, внесенных в кассу наличным либо безналичным способом, при условии, что сумма оплаты будет не менее 5000 (пяти тысяч) рублей.</p>
