      <p style="text-align: center"><u>Восстановление и разблокировка карты</u></p>
      <ol>
          <li>Если блокировка произошла по инициативе участника, то разблокировать карту можно позвонив на Горячую линию и пройдя авторизацию, после чего оператор ПЛ разблокирует карту.</li>
