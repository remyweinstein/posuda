      <p style="text-align: center"><u>Бонусная карта</u></p>
      <ol>
          <li>Карта со статусом Бонусная дает возможность начислять бонусы за совершенные в магазинах компании покупки, и списывать начисленные бонусы для получения скидки при оплате товаров.</li>
          <li>Использовать можно только начисленные и активные бонусы. Срок активации бонусов &ndash; 24 часа с момента покупки.</li>
          <li>За одну операцию можно произвести как списание, так и начисление бонусов. При этом под операцией понимается совершение участником ПЛ покупки в любом из магазинов Компании с использованием карты ПЛ.</li>
      </ol>
      <p>&nbsp;&nbsp;&nbsp;&nbsp; При оформлении покупки сначала происходит списание бонусов (от общей суммы покупки) в зависимости от процента списания, указанного на ценнике, или желаемой участником суммы бонусов к списанию (в количестве меньшем, чем указано на ценнике), затем начисляется установленный процент от итоговой стоимости товара, подлежащей оплате за покупку в рублях после уменьшения цены товара на сумму списанных бонусов. Если в чеке содержится несколько позиций товара с разным процентом списания, то бонусы списываются согласно проценту списания, установленного на конкретный товар. Если количество доступных к списанию бонусов меньше разрешенного к списанию, то бонусы распределяются пропорционально на каждую позицию товара в чеке, за исключением товаров, перечисленных в Приложении № 2.</p>
      <ol start="4">
          <li>Для использования бонусов участник должен пройти идентификацию одним из следующих способов:</li>
      </ol>
      <p>- через МП путем сканирования QR-кода (с применением виртуальной карты); &nbsp;</p>
      <p>- путем предъявления пластиковой карты сотруднику в магазине Компании;</p>
      <p>- с помощью SMS-авторизации по привязанному к карте номеру телефона: ПЛ направляет Участнику на зарегистрированный в ПЛ номер телефона смс-сообщение с кодом. Клиент сообщает полученный код кассиру. Кассир вносит в программу код и авторизует участника.</p>
      <ol start="5">
          <li>Накопленные бонусы можно списать как полностью, так и частично, по желанию участника, но при условии снижения цены товара не ниже уровня минимальных розничных цен (далее &ndash; &laquo;МРЦ&raquo;), установленных Правительством РФ на отдельные виды товара, и не более максимального процента скидки, указанного на ценнике на приобретаемый товар.</li>
          <li>Для списания бонусов участнику необходимо до момента оплаты товара уведомить сотрудника Компании о намерении совершить списание бонусов, предъявить сотруднику на кассе карту участника (пластиковую или виртуальную) либо пройти идентификацию с помощью номера телефона, указанного при регистрации в ПЛ. После предъявления карты либо прохождения идентификации Сотрудник озвучивает сумму бонусов, доступных к списанию, Участник в свою очередь озвучивает сумму, которую желает списать полностью или частично.</li>
          <li>Бонусы, срок действия которых истек, аннулируются путем списания со счета участника в безусловном порядке.</li>
          <li>Аннулированные по истечению срока действия бонусы восстановлению не подлежат.</li>
          <li>В случае возврата участником товара израсходованные на покупку возвращаемого товара бонусы восстанавливаются на бонусном счете участника, а начисленные за покупку возвращаемого товара бонусы аннулируются.</li>
          <li>Бонусы нельзя списать в следующих случаях:</li>
      </ol>
      <p>- при приобретении товаров, не участвующих в системе лояльности (Приложение № 2).</p>
      <p>- при предъявлении не активированной бонусной карты.</p>
      <p>При приобретении товаров, на которые устанавливаются МРЦ, можно списать только то количество бонусов, при котором фактически оплачиваемая рублями стоимость товара не будет ниже МРЦ.</p>
      <ol start="11">
          <li>Лимит операций с использованием бонусной карты составляет 10 (десять) операций в день.</li>
      </ol>
